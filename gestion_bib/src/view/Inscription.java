    private void btn_inscriActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_inscriActionPerformed
        if( tfnom.getText().toString().equals("") || tfprenom.getText().toString().equals("")){
             JOptionPane d = new JOptionPane();
            d.showMessageDialog(this, "Veuillez remplir tous les champs!",
                    "ERROR", JOptionPane.ERROR_MESSAGE);
            return ;
        }
        
        if ((!tfpassword.getText().toString().equals(tfpasswordconfirm.getText().toString()) ) ||( !tfpassword.getText().toString().matches("[a-zA-Z0-9]+"))) {
            JOptionPane d = new JOptionPane();
            d.showMessageDialog(this, "Mot de passe invalide!",
                    "ERROR", JOptionPane.ERROR_MESSAGE);
            return ; 
        }

        if (!tftel.getText().toString().matches("[0-9]{10}")) {
            JOptionPane d = new JOptionPane();
            d.showMessageDialog(this, "Numero invalide !",
                    "ERROR", JOptionPane.ERROR_MESSAGE);
            return ;
        }
        if (!tfemail.getText().toString().matches("[a-zA-Z0-9]*@[a-zA-Z0-9]*.[a-z]*")) {
            JOptionPane d = new JOptionPane();
            d.showMessageDialog(this, "Email invalide !",
                    "ERROR", JOptionPane.ERROR_MESSAGE);
            return ;
        }
        
        Utilisateur u = new Utilisateur(tfnom.getText().toString(),tfprenom.getText().toString(), tfadresse.getText().toString(),tftel.getText().toString(), tfemail.getText().toString(),
                tfpassword.getText().toString(),radioAdmin.isSelected()?'A':'U');
        Cntr_Utilisateur.ajouter(u);
    }//GEN-LAST:event_btn_inscriActionPerformed

        
